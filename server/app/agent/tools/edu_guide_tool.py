async def query_edu_guide(query: str) -> str:
    """
    搜索北京化工大学教务系统使用指南。回答"某某功能在哪""怎么操作""什么时间"等系统使用问题。

    触发: 「怎么查成绩」「在哪选课」「缓考怎么办」「什么时候选课」「校历」「学分替换流程」等。

    Args:
        query: 搜索关键词或问题

    Returns:
        匹配的使用指南文本，或列出所有可查询功能
    """
    query_lower = query.lower()
    results = []

    # 搜索知识条目：匹配键或描述中的关键词
    search_words = (
        query_lower
        .replace("怎么", "")
        .replace("如何", "")
        .replace("在哪", "")
        .replace("在哪", "")
        .replace("什么", "")
        .replace("哪里", "")
        .replace("吗", "")
        .split()
    )

    for key, desc in EDU_GUIDE.items():
        if any(w in key or w in desc for w in search_words if w):
            results.append((key, desc))
        elif query_lower in key or query_lower in desc:
            results.append((key, desc))

    # 精确匹配不到，按分类返回
    if not results:
        for cat, items in CATEGORIES.items():
            if any(w in query_lower for w in [cat] + items):
                lines = [f"【{cat}】"]
                for item in items:
                    lines.append(f"  {item}: {EDU_GUIDE.get(item, '')}")
                return "\n".join(lines)

        # 完全无匹配 → 列出所有可用功能
        lines = [
            f"未找到关于「{query}」的教务系统使用指南。",
            "你可以查询以下功能（或直接提问如「怎么查成绩」「在哪选课」）：",
        ]
        for key, desc in EDU_GUIDE.items():
            lines.append(f"\n【{key}】{desc}")
        return "\n".join(lines)

    # 返回匹配结果（最多5条）
    lines = []
    for key, desc in results[:10]:
        lines.append(f"\n【{key}】{desc}")
    if len(results) > 10:
        lines.append(f"\n...及另外 {len(results) - 10} 条匹配结果")
    return "".join(lines)
